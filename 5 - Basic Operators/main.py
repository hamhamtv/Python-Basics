# Addition, subtraction, multiplication and division
number = 1 + 2 * 3 / 4.0

print(number)



# Leftover of 11 / 3 
# 11/3 is 3 with a leftover of 2
remainder = 11 % 3

print(remainder)



# power relationships (Potenzen)
squared = 7 ** 2
cubed = 7 ** 3

print(squared)
print(cubed)



# Addition of strings concludes in multiple words
helloworld = "hello" + " " + "world" # The doublequoted space in the middle is just to create a space between the words

print(helloworld)



# Multiplying words is possible aswell
lotsofhellos = "hello" * 10
print(lotsofhellos)



# Operators and Lists
even_numbers = [2,4,6,8,10,12]
odd_numbers = [1,3,5,7,9,11]
all_numbers = odd_numbers + even_numbers

print(all_numbers)
print(even_numbers)
print(odd_numbers)

# Multiplying Lists like strings
print([1,2,3] * 3)

